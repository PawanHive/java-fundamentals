// Java boiler code
public class JavaBasics {
  public static void main(String args[]) {

    // print the statement (print)
    System.out.print("Hello World");
    System.out.print("Hello shradha");

    // print the statement (println)
    System.out.println("Hello World");
    System.out.println("Hello World"); // prints the same line

    //print the statement ("\n")
    System.out.println("Hello World \n Hello shradha"); // after "\n" space also counts
    System.out.println("Hello World");
  }
}
