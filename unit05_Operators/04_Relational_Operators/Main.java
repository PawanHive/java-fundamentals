

public class Main {
  public static void main(String[] args) {
    
    // equal to
    int a = 10;
    int b = 10;
    System.out.println(a == b); 

    // not equal to
    int c = 10;
    int d = 10;
    System.out.println(c != d); 

    // less than
    int e = 10;
    int f = 10;
    System.out.println(e < f); 

    // greater than
    int g = 10;
    int h = 10;
    System.out.println(g > h); 

    // less than equal to
    int i = 10;
    int j = 10;
    System.out.println(i <= j); 

    // greater than equal to
    int k = 10;
    int l = 10;
    System.out.println(k >= l); 
  }
}
